class LiveUIStream:
    """Intercepts print statements and routes them to the Rich console live."""
    def __init__(self, console):
        self.console = console
        self.buffer = ""
        # Store the original, un-hijacked stdout so we can print safely
        self.original_stdout = sys.__stdout__ 

    def write(self, text):
        self.buffer += text
        if '\n' in self.buffer:
            lines = self.buffer.split('\n')
            self.buffer = lines.pop()
            for line in lines:
                self._print_colored(line)

    def flush(self):
        if self.buffer:
            self._print_colored(self.buffer)
            self.buffer = ""

    def _print_colored(self, line):
        # We temporarily restore the original stdout just for this print command
        # so Rich doesn't get caught in our trap!
        with redirect_stdout(self.original_stdout):
            if "[DIRECTOR]" in line:
                self.console.print(f"[bold cyan]{line}[/bold cyan]")
            elif "[SCANNER]" in line or "NMAP" in line:
                self.console.print(f"[bold yellow]{line}[/bold yellow]")
            elif "[EXPLOITER]" in line:
                self.console.print(f"[bold red]{line}[/bold red]")
            elif "[DEFENDER]" in line or "SOC ALERT" in line:
                self.console.print(f"[bold blue]{line}[/bold blue]")
            elif "[PATCHER]" in line:
                self.console.print(f"[bold green]{line}[/bold green]")
            elif line.strip(): 
                self.console.print(line)

